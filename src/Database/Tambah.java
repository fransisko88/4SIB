    private void btn_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hapusActionPerformed
        String nim1 = txtNim.getText();
        int selectedOption =  JOptionPane.showConfirmDialog(null, "Hapus data dengan Nim : "+nim1+"?","Konfirmasi",JOptionPane.YES_NO_OPTION);
        if (selectedOption == JOptionPane.YES_OPTION){
            Connection c = koneksi_db.getKoneksi();
            try{
                String sql = "delete from tbl_mahasiswa where nim=?";
                 pst=c.prepareStatement(sql);
                 pst.setString(1, nim1);
                 pst.executeUpdate();
                 
//                 Statement s = c.createStatement();

                 JOptionPane.showMessageDialog(this, "Data berhasil diHapus","Pemberitahuan",JOptionPane.INFORMATION_MESSAGE);
                 new Form_mahasiswa("").setVisible(true);
                 this.dispose();
             }
             catch(HeadlessException | SQLException e){
                 JOptionPane.showMessageDialog(null,e);
             } 
                 
         } 
   
    }//GEN-LAST:event_btn_hapusActionPerformed

