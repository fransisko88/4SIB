    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // TODO add your handling code here:
        String nim1 = txtNim.getText();
        String nama1 = txtNama.getText();
        String alamat1 = txtAlamat.getText();
        String radPria = rPria.getText();
        String radWanita = rWanita.getText();
        String kls = (String)cKelas.getSelectedItem();
        

        Connection c = koneksi_db.getKoneksi();

        String sql = "Insert into tbl_mahasiswa values(?,?,?,?,?)";
        try{
            pst = c.prepareStatement(sql);
            pst.setString(1, nim1);
            pst.setString(2, nama1);
           
            if(rPria.isSelected()){
               pst.setString(3, radPria);
            }
            else if(rWanita.isSelected()){
               pst.setString(3, radWanita); 
            }
            else{
              JOptionPane.showMessageDialog(null,"ISI JENIS KELAMIN !");
            }
             pst.setString(4, kls);
             pst.setString(5, alamat1);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Sukses Input Data");
            
            new Form_mahasiswa("").setVisible(true);
            this.dispose();
        }
        catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

